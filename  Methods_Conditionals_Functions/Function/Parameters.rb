# What is parameter?
# Methods on objects can accept one or more inputs.
# The technical word for an input in programming language is an argument.
# A parameter is the placeholder name assigned to an expected argument.

# Example:
# Method | (Parameters) | Return value
p 3.between?(1, 5)  # returns true
p 6.between?(1, 5) #returns false
p 'cat'.between?('ant', 'dog') #returns true