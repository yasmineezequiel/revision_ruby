# print is a bit different from puts as it gathers all the string into one:

print "Hello"
print "Whatsapp"
print "poop"
#result:
# HelloWhatsapppoop

# an option is to add a space so the output will be separated:
print "Hello "
print " Whatsapp "
print " poop "
#output:
# Hello  Whatsapp  poop

# or add an empty string between:
print "Hello"
print " "
print "Whatsapp"
print " "
print "poop"
# result:
# Hello Whatsapp poop